export default function Card(title, link, text) {
    return `<div class="card-text-box">
                <div class="box-title-link">
                    <h2>${title}</h2>
                    <a href="${link}">
                        <i class="ri-arrow-right-up-box-line"></i>
                    </a>
                </div>
                <p>${text}</p>
            </div>`
}

